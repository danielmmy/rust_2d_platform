/// A teleporter pad, stored as pure coordinates: stepping on the cell
/// `(origin_col, origin_row)` sends the player to room `to` at `(dest_col,
/// dest_row)`. All are grid cells (row 0 = top, as written in `tiles`). Because a
/// pad needs no grid glyph, it never competes with tile characters — a room may
/// hold many pads, and a pad may even target another cell in its own room.
#[derive(Clone)]
pub struct Teleport {
    pub origin_col: i32,
    pub origin_row: i32,
    pub to: String,
    pub dest_col: i32,
    pub dest_row: i32,
}

/// Types an enemy `E` glyph at cell `(col, row)` (grid coords, row 0 = top). The
/// `kind` indexes [`ENEMY_KINDS`](crate::combat::ENEMY_KINDS); an `E` with no
/// matching entry falls back to kind 0. One glyph, any number of types.
#[derive(Clone)]
pub struct EnemySpawn {
    pub kind: usize,
    pub col: i32,
    pub row: i32,
}

/// A one-way passage out of a room. Walking off the edge nearest `origin` loads room
/// `to` and drops the player at cell `dest`. An edge may carry several doors (e.g. a
/// top and a bottom doorway); the one used is whichever door's `origin` is nearest the
/// point where the player crossed. All cells are grid coords (row 0 = top). In RON a
/// door is the tuple `((origin_col, origin_row), "to", (dest_col, dest_row))`.
#[derive(Clone)]
pub struct Door {
    pub origin: (i32, i32),
    pub to: String,
    pub dest: (i32, i32),
}

/// Read a `(col, row)` cell tuple from a parsed value.
fn parse_cell(v: &ron::Value) -> Result<(i32, i32), RonError> {
    let c = v.as_list()?;
    if c.len() != 2 {
        return Err(RonError("a cell must be (col, row)".into()));
    }
    Ok((c[0].as_i32()?, c[1].as_i32()?))
}

/// Read one door tuple `((ocol, orow), "to", (dcol, drow))` from a parsed value.
fn parse_door(v: &ron::Value) -> Result<Door, RonError> {
    let items = v.as_list()?;
    if items.len() != 3 {
        return Err(RonError(
            "a door must be ((col, row), \"room\", (col, row))".into(),
        ));
    }
    Ok(Door {
        origin: parse_cell(&items[0])?,
        to: items[1].as_str()?.to_string(),
        dest: parse_cell(&items[2])?,
    })
}

/// How a [`Mover`] cycles its path once it reaches the last stop.
#[derive(Clone, Copy, PartialEq, Default)]
pub enum MoveMode {
    /// Travel to the last stop and halt there.
    Once,
    /// Cycle home → stops → home → … forever.
    #[default]
    Loop,
    /// Bounce back and forth along the path.
    PingPong,
}

/// A moving platform: a rigid group of `tiles` (cells, row 0 = top) whose anchor
/// (`tiles[0]`) travels through `path` carrying every tile by the same offset. It starts
/// at its `tiles` home, visits each `path` stop in order at `speed` px/s, pausing `rest`
/// ms at each, then continues per [`MoveMode`]. In RON:
/// `(tiles: [(c,r),…], path: [(c,r),…], mode: "loop", speed: 60, rest: 1000)`.
#[derive(Clone)]
pub struct Mover {
    pub tiles: Vec<(i32, i32)>,
    pub path: Vec<(i32, i32)>,
    pub mode: MoveMode,
    pub speed: f32,
    pub rest: f32,
}

/// Read one mover struct from a parsed value.
fn parse_mover(v: &ron::Value) -> Result<Mover, RonError> {
    let cells = |name: &str| -> Result<Vec<(i32, i32)>, RonError> {
        match v.try_field(name) {
            Some(list) => list.as_list()?.iter().map(parse_cell).collect(),
            None => Ok(Vec::new()),
        }
    };
    let tiles = cells("tiles")?;
    if tiles.is_empty() {
        return Err(RonError("a mover needs at least one tile".into()));
    }
    let mode = match v.try_field("mode").and_then(|m| m.as_str().ok()) {
        Some("once") => MoveMode::Once,
        Some("pingpong") => MoveMode::PingPong,
        _ => MoveMode::Loop,
    };
    Ok(Mover {
        tiles,
        path: cells("path")?,
        mode,
        speed: v
            .try_field("speed")
            .and_then(|s| s.as_f32().ok())
            .unwrap_or(60.0),
        rest: v
            .try_field("rest")
            .and_then(|s| s.as_f32().ok())
            .unwrap_or(0.0),
    })
}

/// Per-layer scenery selection: each parallax layer picks a **set** by name (empty =
/// that layer is absent), so a room can mix elements — e.g. a desert sky over forest
/// hills. The image for a layer is `"<set>/<layer>.png"`. See [`crate::scenery`].
#[derive(Clone, Default)]
pub struct Scenery {
    pub far: String,
    pub mid: String,
    pub near: String,
    pub fg: String,
}

/// Parse a `scenery` value: the per-layer struct `(far: "...", mid: ..., near: ..., fg:
/// ...)`, or a bare set name string (legacy) used for every layer.
fn parse_scenery(value: Option<&ron::Value>) -> Scenery {
    let Some(v) = value else {
        return Scenery::default();
    };
    if let Ok(set) = v.as_str() {
        let s = set.to_string();
        return Scenery {
            far: s.clone(),
            mid: s.clone(),
            near: s.clone(),
            fg: s,
        };
    }
    let layer = |name: &str| {
        v.try_field(name)
            .and_then(|x| x.as_str().ok())
            .unwrap_or("")
            .to_string()
    };
    Scenery {
        far: layer("far"),
        mid: layer("mid"),
        near: layer("near"),
        fg: layer("fg"),
    }
}

/// A combatant in a room's **arena** (its `fog_wall` list). These spawn only once the
/// player enters the room, and crossing the fog seals the exits until all are dead
/// (see [`crate::boss`]). `boss` picks a boss over a normal enemy of `kind`.
#[derive(Clone)]
pub struct ArenaSpawn {
    pub boss: bool,
    pub kind: usize,
    pub col: i32,
    pub row: i32,
}

/// One map's data, read from a `.map.ron` file by [`MapLoader`].
#[derive(Asset, TypePath, Clone)]
pub struct MapData {
    /// A human-friendly display name (empty → falls back to the file key).
    pub name: String,
    /// Characters treated as solid tiles (e.g. `"#"`).
    pub solid: String,
    /// Characters that are deadly ground spikes (e.g. `"^"`).
    pub spikes: String,
    /// Characters that spawn falling rocks (e.g. `"R"`).
    pub rocks: String,
    /// Doors off the top edge (empty = a solid / bottomless edge). See [`Door`].
    pub north: Vec<Door>,
    /// Doors off the bottom edge.
    pub south: Vec<Door>,
    /// Doors off the right edge.
    pub east: Vec<Door>,
    /// Doors off the left edge.
    pub west: Vec<Door>,
    /// Teleporter pads in this room (empty = none).
    pub teleports: Vec<Teleport>,
    /// Per-cell enemy types for `E` glyphs (cells without an entry use kind 0).
    pub enemies: Vec<EnemySpawn>,
    /// Arena combatants (empty = not an arena). Spawn on entry; crossing the room's
    /// fog (`F` glyphs) seals the exits until all of these are dead.
    pub fog_wall: Vec<ArenaSpawn>,
    /// If set, this arena **re-arms on a bench rest** (its foes respawn); otherwise it
    /// stays cleared permanently. Bosses persist regardless (their kill is saved).
    pub fog_respawn: bool,
    /// Moving platforms (empty = none). See [`Mover`].
    pub movers: Vec<Mover>,
    /// Per-layer parallax scenery for this room (mix-and-match; see [`Scenery`]).
    pub scenery: Scenery,
    /// Background (clear) colour as `[r, g, b]` in 0..1.
    pub bg: [f32; 3],
    /// The grid, one string per row (top to bottom).
    pub tiles: Vec<String>,
}

impl MapData {
    /// Build a map from RON text (see [`crate::ron`] for the supported subset).
    pub(crate) fn from_ron(text: &str) -> Result<Self, RonError> {
        let value = ron::from_str(text)?;

        let optional_str = |name: &str| -> Result<String, RonError> {
            match value.try_field(name) {
                Some(v) => Ok(v.as_str()?.to_string()),
                None => Ok(String::new()),
            }
        };

        let bg_list = value.field("bg")?.as_list()?;
        if bg_list.len() != 3 {
            return Err(RonError("bg must have 3 components".into()));
        }
        let bg = [
            bg_list[0].as_f32()?,
            bg_list[1].as_f32()?,
            bg_list[2].as_f32()?,
        ];

        let tiles = value
            .field("tiles")?
            .as_list()?
            .iter()
            .map(|v| Ok(v.as_str()?.to_string()))
            .collect::<Result<Vec<_>, RonError>>()?;

        let teleports = match value.try_field("teleports") {
            Some(list) => list
                .as_list()?
                .iter()
                .map(|t| {
                    Ok(Teleport {
                        origin_col: t.field("origin_col")?.as_i32()?,
                        origin_row: t.field("origin_row")?.as_i32()?,
                        to: t.field("to")?.as_str()?.to_string(),
                        dest_col: t.field("dest_col")?.as_i32()?,
                        dest_row: t.field("dest_row")?.as_i32()?,
                    })
                })
                .collect::<Result<Vec<_>, RonError>>()?,
            None => Vec::new(),
        };

        let enemies = match value.try_field("enemies") {
            Some(list) => list
                .as_list()?
                .iter()
                .map(|e| {
                    Ok(EnemySpawn {
                        kind: e.field("kind")?.as_i32()?.max(0) as usize,
                        col: e.field("col")?.as_i32()?,
                        row: e.field("row")?.as_i32()?,
                    })
                })
                .collect::<Result<Vec<_>, RonError>>()?,
            None => Vec::new(),
        };

        let fog_wall = match value.try_field("fog_wall") {
            Some(list) => list
                .as_list()?
                .iter()
                .map(|c| {
                    Ok(ArenaSpawn {
                        boss: c
                            .try_field("boss")
                            .and_then(|v| v.as_i32().ok())
                            .unwrap_or(0)
                            != 0,
                        kind: c.field("kind")?.as_i32()?.max(0) as usize,
                        col: c.field("col")?.as_i32()?,
                        row: c.field("row")?.as_i32()?,
                    })
                })
                .collect::<Result<Vec<_>, RonError>>()?,
            None => Vec::new(),
        };

        let fog_respawn = value
            .try_field("fog_respawn")
            .and_then(|v| v.as_i32().ok())
            .unwrap_or(0)
            != 0;

        let movers = match value.try_field("movers") {
            Some(list) => list
                .as_list()?
                .iter()
                .map(parse_mover)
                .collect::<Result<Vec<_>, RonError>>()?,
            None => Vec::new(),
        };

        // Each edge is a list of doors, `((ocol, orow), "to", (dcol, drow))`.
        let doors = |name: &str| -> Result<Vec<Door>, RonError> {
            match value.try_field(name) {
                Some(list) => list.as_list()?.iter().map(parse_door).collect(),
                None => Ok(Vec::new()),
            }
        };

        Ok(MapData {
            name: optional_str("name")?,
            solid: value.field("solid")?.as_str()?.to_string(),
            spikes: optional_str("spikes")?,
            rocks: optional_str("rocks")?,
            north: doors("north")?,
            south: doors("south")?,
            east: doors("east")?,
            west: doors("west")?,
            teleports,
            enemies,
            fog_wall,
            fog_respawn,
            movers,
            scenery: parse_scenery(value.try_field("scenery")),
            bg,
            tiles,
        })
    }

    fn bg_color(&self) -> Color {
        Color::srgb(self.bg[0], self.bg[1], self.bg[2])
    }

    /// The display name, or the file `key` when none has been set.
    pub(crate) fn display_name<'a>(&'a self, key: &'a str) -> &'a str {
        if self.name.is_empty() {
            key
        } else {
            &self.name
        }
    }

    /// Serialise back to the `.map.ron` text our reader accepts (for the editor).
    pub(crate) fn to_ron(&self) -> String {
        let rows: String = self
            .tiles
            .iter()
            .map(|row| format!("        \"{row}\",\n"))
            .collect();
        let teleports: String = self
            .teleports
            .iter()
            .map(|t| {
                format!(
                    "        (origin_col: {}, origin_row: {}, to: \"{}\", dest_col: {}, dest_row: {}),\n",
                    t.origin_col, t.origin_row, t.to, t.dest_col, t.dest_row
                )
            })
            .collect();
        let enemies: String = self
            .enemies
            .iter()
            .map(|e| {
                format!(
                    "        (kind: {}, col: {}, row: {}),\n",
                    e.kind, e.col, e.row
                )
            })
            .collect();
        let fog_wall: String = self
            .fog_wall
            .iter()
            .map(|c| {
                format!(
                    "        (boss: {}, kind: {}, col: {}, row: {}),\n",
                    i32::from(c.boss),
                    c.kind,
                    c.col,
                    c.row
                )
            })
            .collect();
        // Each edge renders as a list of door tuples, one per line.
        let door_list = |doors: &[Door]| -> String {
            doors
                .iter()
                .map(|d| {
                    format!(
                        "        (({}, {}), \"{}\", ({}, {})),\n",
                        d.origin.0, d.origin.1, d.to, d.dest.0, d.dest.1
                    )
                })
                .collect()
        };
        let north = door_list(&self.north);
        let south = door_list(&self.south);
        let east = door_list(&self.east);
        let west = door_list(&self.west);
        // Movers: a `(col, row)` cell list, then one struct per platform.
        let cell_list = |cells: &[(i32, i32)]| -> String {
            let inner: Vec<String> = cells.iter().map(|(c, r)| format!("({c}, {r})")).collect();
            format!("[{}]", inner.join(", "))
        };
        let movers: String = self
            .movers
            .iter()
            .map(|m| {
                let mode = match m.mode {
                    MoveMode::Once => "once",
                    MoveMode::PingPong => "pingpong",
                    MoveMode::Loop => "loop",
                };
                format!(
                    "        (tiles: {}, path: {}, mode: \"{}\", speed: {}, rest: {}),\n",
                    cell_list(&m.tiles),
                    cell_list(&m.path),
                    mode,
                    m.speed,
                    m.rest
                )
            })
            .collect();
        format!(
            "(\n    name: \"{}\",\n    solid: \"{}\",\n    spikes: \"{}\",\n    rocks: \"{}\",\n    \
             north: [\n{north}    ],\n    south: [\n{south}    ],\n    \
             east: [\n{east}    ],\n    west: [\n{west}    ],\n    \
             teleports: [\n{teleports}    ],\n    enemies: [\n{enemies}    ],\n    \
             fog_wall: [\n{fog_wall}    ],\n    fog_respawn: {},\n    \
             movers: [\n{movers}    ],\n    \
             scenery: (far: \"{}\", mid: \"{}\", near: \"{}\", fg: \"{}\"),\n    \
             bg: [{}, {}, {}],\n    tiles: [\n{rows}    ],\n)\n",
            self.name,
            self.solid,
            self.spikes,
            self.rocks,
            i32::from(self.fog_respawn),
            self.scenery.far,
            self.scenery.mid,
            self.scenery.near,
            self.scenery.fg,
            self.bg[0],
            self.bg[1],
            self.bg[2],
        )
    }

    /// Every room this map links out to (across all four edges).
    #[cfg(test)]
    fn linked_rooms(&self) -> impl Iterator<Item = &str> {
        [&self.north, &self.south, &self.east, &self.west]
            .into_iter()
            .flatten()
            .map(|d| d.to.as_str())
    }
}

/// Loads [`MapData`] from `assets/maps/*.map.ron` using our own RON reader, so the
/// game depends only on Bevy (no `serde`/`ron`/`bevy_common_assets`).
#[derive(Default, TypePath)]
struct MapLoader;

impl AssetLoader for MapLoader {
    type Asset = MapData;
    type Settings = ();
    type Error = RonError;

    async fn load(
        &self,
        reader: &mut dyn Reader,
        _settings: &(),
        _ctx: &mut LoadContext<'_>,
    ) -> Result<MapData, RonError> {
        let mut bytes = Vec::new();
        reader
            .read_to_end(&mut bytes)
            .await
            .map_err(|e| RonError(format!("read error: {e}")))?;
        let text = std::str::from_utf8(&bytes).map_err(|e| RonError(format!("not UTF-8: {e}")))?;
        MapData::from_ron(text)
    }

    fn extensions(&self) -> &[&str] {
        &["map.ron"]
    }
}

/// How the player should be placed when a room loads.
#[derive(Clone)]
pub(crate) enum Entry {
    /// The world's starting position (the `@` marker in the grid).
    Start,
    /// Arrived by walking through a [`Door`]; place the player at the door's
    /// destination cell `(col, row)` (grid coords, row 0 = top).
    Edge(i32, i32),
    /// Arrived through a teleporter; place the player at the destination cell
    /// `(col, row)` (grid coords, row 0 = top) recorded on the pad.
    Teleport(i32, i32),
    /// Respawned/loaded onto a bench at cell `(col, row)` (grid coords).
    Bench(i32, i32),
}

