    /// The binary must carry a complete, valid Story campaign **and** every sprite the
    /// game asks for — so a release exe runs with no `assets/` folder. (No disk or GPU:
    /// `from_buffer` decodes on the CPU.)
    #[test]
    fn embedded_assets_are_self_contained() {
        assert!(!EMBEDDED_STORY_MAPS.is_empty(), "no embedded rooms");
        assert!(
            EMBEDDED_STORY_MAPS.iter().any(|(n, _)| *n == START_MAP),
            "embedded campaign is missing the start room"
        );
        for (name, ron) in EMBEDDED_STORY_MAPS {
            MapData::from_ron(ron).unwrap_or_else(|e| panic!("embedded room '{name}': {e}"));
        }

        let mut wanted = vec![
            "tile.png",
            "player.png",
            "spikes.png",
            "portal.png",
            "bench.png",
            "orb.png",
            "slash.png",
            "rock.png",
            "boss.png",
        ];
        wanted.extend(ENEMY_KINDS.iter().map(|k| k.sheet));
        let mut images = Assets::<Image>::default();
        for name in wanted {
            // Panics with a clear message if a sprite is missing or won't decode.
            embedded_sprite(&mut images, name);
        }
    }

    /// Every room on disk must parse with our RON reader, have content, and only
    /// name neighbours that actually exist.
    #[test]
    fn demo_maps_parse_and_interconnect() {
        let names = discover_rooms(SHIPPED_MAPS_DIR);
        assert!(
            names.len() >= 12,
            "expected at least the 12 demo rooms, found {}",
            names.len()
        );

        let mut maps = HashMap::new();
        for name in &names {
            let path = map_fs_path(SHIPPED_MAPS_DIR, name);
            let text =
                std::fs::read_to_string(&path).unwrap_or_else(|e| panic!("reading {path}: {e}"));
            let map = MapData::from_ron(&text).unwrap_or_else(|e| panic!("parsing {path}: {e}"));

            assert!(!map.solid.is_empty(), "{name}: empty solid legend");
            assert!(!map.tiles.is_empty(), "{name}: no rows");
            maps.insert(name.clone(), map);
        }

        // Every declared neighbour must reference a real room. (Links need not be
        // symmetric — one-way passages are allowed, and the builder can author
        // rooms — but a dangling link is a bug.)
        for (name, map) in &maps {
            for target in map.linked_rooms() {
                assert!(
                    maps.contains_key(target),
                    "{name}: links to unknown room '{target}'"
                );
            }
            // Teleporters: origin must be in this room and dest in a real room.
            let dims = |m: &MapData| {
                (
                    m.tiles.iter().map(|r| r.chars().count()).max().unwrap_or(0) as i32,
                    m.tiles.len() as i32,
                )
            };
            let (ow, oh) = dims(map);
            for tp in &map.teleports {
                assert!(
                    (0..ow).contains(&tp.origin_col) && (0..oh).contains(&tp.origin_row),
                    "{name}: teleporter origin ({}, {}) out of bounds",
                    tp.origin_col,
                    tp.origin_row
                );
                let dest = maps
                    .get(&tp.to)
                    .unwrap_or_else(|| panic!("{name}: teleporter to unknown room '{}'", tp.to));
                let (dw, dh) = dims(dest);
                assert!(
                    (0..dw).contains(&tp.dest_col) && (0..dh).contains(&tp.dest_row),
                    "{name}: teleporter dest ({}, {}) out of bounds in '{}'",
                    tp.dest_col,
                    tp.dest_row,
                    tp.to
                );
            }
            // Enemy entries must use a real kind and sit on an `E` cell.
            for e in &map.enemies {
                assert!(
                    e.kind < ENEMY_KINDS.len(),
                    "{name}: enemy kind {} out of range",
                    e.kind
                );
                let on_enemy = map
                    .tiles
                    .get(e.row as usize)
                    .and_then(|line| line.chars().nth(e.col as usize))
                    == Some(ENEMY_GLYPH);
                assert!(
                    on_enemy,
                    "{name}: enemy entry ({}, {}) not on an 'E'",
                    e.col, e.row
                );
            }
        }

        // The starting room must contain the start marker.
        let start = &maps[START_MAP];
        assert!(
            start.tiles.iter().any(|r| r.contains(START_MARKER)),
            "{START_MAP}: missing start marker '{START_MARKER}'"
        );
    }

    /// What the level builder writes must round-trip back through our reader.
    #[test]
    fn to_ron_round_trips() {
        let original = MapData {
            name: "Forest Glade".to_string(),
            solid: "#".to_string(),
            spikes: "^".to_string(),
            rocks: "R".to_string(),
            north: vec![Door {
                origin: (1, 0),
                to: "r0_1".to_string(),
                dest: (1, 2),
            }],
            south: Vec::new(),
            east: vec![
                Door {
                    origin: (3, 1),
                    to: "r1_0".to_string(),
                    dest: (0, 1),
                },
                Door {
                    origin: (3, 2),
                    to: "r1_0".to_string(),
                    dest: (0, 2),
                },
            ],
            west: Vec::new(),
            teleports: vec![Teleport {
                origin_col: 1,
                origin_row: 1,
                to: "r1_1".to_string(),
                dest_col: 7,
                dest_row: 2,
            }],
            enemies: vec![EnemySpawn {
                kind: 1,
                col: 2,
                row: 2,
            }],
            fog_wall: vec![ArenaSpawn {
                boss: true,
                kind: 0,
                col: 5,
                row: 6,
            }],
            fog_respawn: true,
            movers: vec![Mover {
                tiles: vec![(5, 5), (6, 5)],
                path: vec![(10, 5), (10, 8)],
                mode: MoveMode::PingPong,
                speed: 60.0,
                rest: 1000.0,
            }],
            scenery: Scenery {
                far: "snowy_mountains".to_string(),
                mid: "forest_meadow".to_string(),
                near: "forest_meadow".to_string(),
                fg: "misty_swamp".to_string(),
            },
            bg: [0.25, 0.5, 0.75],
            tiles: vec![
                "####".to_string(),
                "#T@#".to_string(),
                "#^E#".to_string(),
                "####".to_string(),
            ],
        };

        let parsed = MapData::from_ron(&original.to_ron()).expect("round-trip parse");
        assert_eq!(parsed.name, original.name);
        assert_eq!(parsed.solid, original.solid);
        assert_eq!(parsed.bg, original.bg);
        assert_eq!(parsed.tiles, original.tiles);
        // Doors round-trip: a single north door and two east doors to the same room.
        assert_eq!(parsed.north.len(), 1);
        assert_eq!(parsed.north[0].origin, (1, 0));
        assert_eq!(parsed.north[0].to, "r0_1");
        assert_eq!(parsed.north[0].dest, (1, 2));
        assert!(parsed.south.is_empty());
        assert_eq!(parsed.east.len(), 2);
        assert_eq!(parsed.east[0].origin, (3, 1));
        assert_eq!(parsed.east[1].dest, (0, 2));
        assert!(parsed.west.is_empty());
        assert_eq!(parsed.teleports.len(), 1);
        assert_eq!(parsed.teleports[0].origin_col, 1);
        assert_eq!(parsed.teleports[0].origin_row, 1);
        assert_eq!(parsed.teleports[0].to, "r1_1");
        assert_eq!(parsed.teleports[0].dest_col, 7);
        assert_eq!(parsed.teleports[0].dest_row, 2);
        assert_eq!(parsed.enemies.len(), 1);
        assert_eq!(parsed.enemies[0].kind, 1);
        assert_eq!(parsed.enemies[0].col, 2);
        assert_eq!(parsed.enemies[0].row, 2);
        assert_eq!(parsed.fog_wall.len(), 1);
        assert!(parsed.fog_wall[0].boss);
        assert_eq!(parsed.fog_wall[0].col, 5);
        assert_eq!(parsed.fog_wall[0].row, 6);
        // Movers round-trip: tiles, path, mode, speed, rest.
        assert_eq!(parsed.movers.len(), 1);
        assert_eq!(parsed.movers[0].tiles, vec![(5, 5), (6, 5)]);
        assert_eq!(parsed.movers[0].path, vec![(10, 5), (10, 8)]);
        assert!(parsed.movers[0].mode == MoveMode::PingPong);
        assert_eq!(parsed.movers[0].speed, 60.0);
        assert_eq!(parsed.movers[0].rest, 1000.0);
        // Scenery round-trips per layer (mix-and-match).
        assert_eq!(parsed.scenery.far, "snowy_mountains");
        assert_eq!(parsed.scenery.mid, "forest_meadow");
        assert_eq!(parsed.scenery.near, "forest_meadow");
        assert_eq!(parsed.scenery.fg, "misty_swamp");
    }
