/// Where the active save's rooms come from: the binary's embedded Story campaign, or
/// a Builder save's on-disk, editable copy.
#[derive(Resource, Default)]
pub(crate) enum LevelRoot {
    /// Read the shipped Story levels from [`EMBEDDED_STORY_MAPS`] (no `assets/` needed).
    #[default]
    Story,
    /// A Builder save's level directory (read from and written to on disk).
    Builder(String),
}

impl LevelRoot {
    /// The on-disk directory, if this is a Builder world (the editor writes here).
    pub(crate) fn dir(&self) -> Option<&str> {
        match self {
            LevelRoot::Story => None,
            LevelRoot::Builder(dir) => Some(dir),
        }
    }
}

/// A Builder slot's private level directory.
pub(crate) fn builder_maps_dir(slot: usize) -> String {
    format!("saves/builder{slot}/maps")
}

/// (Re)seed a Builder slot from the embedded Story levels and return its directory.
/// Wipes any previous contents so "New Game" over an old slot starts clean.
pub(crate) fn init_builder_world(slot: usize) -> String {
    let dir = builder_maps_dir(slot);
    let _ = std::fs::remove_dir_all(&dir);
    let _ = std::fs::create_dir_all(&dir);
    for (name, ron) in EMBEDDED_STORY_MAPS {
        let _ = std::fs::write(map_fs_path(&dir, name), ron);
    }
    dir
}

/// Filesystem path to a room's file under a Builder level directory.
pub(crate) fn map_fs_path(dir: &str, name: &str) -> String {
    format!("{dir}/{name}.map.ron")
}

/// Every `*.map.ron` in a Builder directory, sorted.
pub(crate) fn discover_rooms(dir: &str) -> Vec<String> {
    let mut names: Vec<String> = std::fs::read_dir(dir)
        .into_iter()
        .flatten()
        .flatten()
        .filter_map(|entry| {
            entry
                .file_name()
                .to_str()
                .and_then(|n| n.strip_suffix(".map.ron"))
                .map(str::to_string)
        })
        .collect();
    names.sort();
    names
}
/// Grid character marking the player's start position in the starting room.
pub(crate) const START_MARKER: char = '@';
pub struct WorldPlugin;

impl Plugin for WorldPlugin {
    fn build(&self, app: &mut App) {
        app.init_asset::<MapData>()
            .register_asset_loader(MapLoader)
            .add_message::<LoadMap>()
            .init_resource::<Solids>()
            .init_resource::<TransitionCooldown>()
            .init_resource::<CurrentRoom>()
            .init_resource::<RoomView>()
            .init_resource::<TeleportArmed>()
            .init_resource::<PendingSpawn>()
            .init_resource::<LevelRoot>()
            .add_systems(Startup, load_assets)
            .add_systems(OnEnter(GameState::Loading), load_world)
            .add_systems(Update, wait_for_load.run_if(in_state(GameState::Loading)))
            .add_systems(
                OnEnter(GameState::Playing),
                (enter_playing, spawn_bench_prompt),
            )
            .add_systems(OnExit(GameState::Playing), despawn_bench_prompt)
            .add_systems(
                Update,
                (
                    handle_load_map,
                    tick_cooldown,
                    confine_to_arena.in_set(GameSet::Transitions),
                    detect_transitions.in_set(GameSet::Transitions),
                    detect_teleport.in_set(GameSet::Transitions),
                    bench_interact.in_set(GameSet::Transitions),
                )
                    .run_if(in_state(GameState::Playing)),
            );
    }
}

/// Decode PNG bytes into a live `Image` asset, keeping the game's crisp
/// nearest-neighbour sampling.
fn decode_png(images: &mut Assets<Image>, file: &str, bytes: &[u8]) -> Handle<Image> {
    let image = Image::from_buffer(
        bytes,
        ImageType::Extension("png"),
        CompressedImageFormats::NONE,
        true, // sRGB colour art
        ImageSampler::nearest(),
        RenderAssetUsages::RENDER_WORLD,
    )
    .unwrap_or_else(|e| panic!("decoding embedded PNG '{file}': {e:?}"));
    images.add(image)
}

/// Decode an embedded sprite by file name.
fn embedded_sprite(images: &mut Assets<Image>, file: &str) -> Handle<Image> {
    let bytes = EMBEDDED_SPRITES
        .iter()
        .find(|(name, _)| *name == file)
        .map(|(_, bytes)| *bytes)
        .unwrap_or_else(|| panic!("embedded sprite '{file}' not found"));
    decode_png(images, file, bytes)
}

fn load_assets(mut commands: Commands, mut images: ResMut<Assets<Image>>) {
    // Sprites are decoded from the embedded bytes; rooms come from the active save
    // (see `load_world`), so `maps`/`room_names` start empty here.
    commands.insert_resource(GameAssets {
        maps: HashMap::new(),
        room_names: Vec::new(),
        tile: embedded_sprite(&mut images, "tile.png"),
        player: embedded_sprite(&mut images, "player.png"),
        spikes: embedded_sprite(&mut images, "spikes.png"),
        portal: embedded_sprite(&mut images, "portal.png"),
        bench: embedded_sprite(&mut images, "bench.png"),
        enemy_sheets: ENEMY_KINDS
            .iter()
            .map(|k| embedded_sprite(&mut images, k.sheet))
            .collect(),
        orb: embedded_sprite(&mut images, "orb.png"),
        slash: embedded_sprite(&mut images, "slash.png"),
        boss: embedded_sprite(&mut images, "boss.png"),
        scenery: EMBEDDED_SCENERY
            .iter()
            .map(|(name, bytes)| (name.to_string(), decode_png(&mut images, name, bytes)))
            .collect(),
    });
    commands.insert_resource(RockSprite(embedded_sprite(&mut images, "rock.png")));
}

/// Load the active save's rooms into live `MapData` assets — the embedded Story
/// campaign, or a Builder save's on-disk copy. Runs on entering `Loading`, so it
/// always reflects the world the just-selected save points at.
fn load_world(
    level_root: Res<LevelRoot>,
    mut assets: ResMut<GameAssets>,
    mut maps: ResMut<Assets<MapData>>,
) {
    assets.maps.clear();
    assets.room_names.clear();

    let rooms: Vec<(String, MapData)> = match &*level_root {
        LevelRoot::Story => EMBEDDED_STORY_MAPS
            .iter()
            .filter_map(|(name, ron)| match MapData::from_ron(ron) {
                Ok(map) => Some((name.to_string(), map)),
                Err(e) => {
                    warn!("embedded room '{name}' failed to parse: {e}");
                    None
                }
            })
            .collect(),
        LevelRoot::Builder(dir) => discover_rooms(dir)
            .into_iter()
            .filter_map(|name| {
                let path = map_fs_path(dir, &name);
                match std::fs::read_to_string(&path).map(|t| MapData::from_ron(&t)) {
                    Ok(Ok(map)) => Some((name, map)),
                    _ => {
                        warn!("could not load room '{name}' from {path}");
                        None
                    }
                }
            })
            .collect(),
    };

    for (name, map) in rooms {
        let handle = maps.add(map);
        assets.maps.insert(name.clone(), handle);
        assets.room_names.push(name);
    }
    assets.room_names.sort();
}

fn wait_for_load(mut next: ResMut<NextState<GameState>>) {
    // Sprites are decoded up-front and rooms loaded in `load_world`, so there's
    // nothing async to wait on — head straight into play.
    next.set(GameState::Playing);
}

fn enter_playing(
    mut commands: Commands,
    assets: Res<GameAssets>,
    current: Res<CurrentRoom>,
    mut pending: ResMut<PendingSpawn>,
    mut health: ResMut<Health>,
    players: Query<(), With<Player>>,
    mut load: MessageWriter<LoadMap>,
) {
    // Spawn the player once; `handle_load_map` positions it for each room. (We
    // re-enter `Playing` after the level builder, so it may already exist.)
    if players.is_empty() {
        commands.spawn((
            Player,
            Velocity::default(),
            JumpState::default(),
            Sprite {
                image: assets.player.clone(),
                custom_size: Some(Vec2::new(24.0, 40.0)),
                ..default()
            },
            Transform::from_xyz(0.0, 0.0, 10.0),
        ));
    }

    // A menu-driven New Game / Load Game requests a room (and maybe a bench cell);
    // honour it and refill hearts. Otherwise we're returning from the editor — just
    // reload the current room, falling back to the start (the builder can move rooms).
    if let Some(req) = pending.0.take() {
        health.current = health.max;
        let entry = match req.at_cell {
            Some((col, row)) => Entry::Bench(col, row),
            None => Entry::Start,
        };
        let map = if assets.maps.contains_key(&req.room) {
            req.room
        } else {
            START_MAP.to_string()
        };
        load.write(LoadMap { map, entry });
        return;
    }

    let map = if assets.maps.contains_key(&current.name) {
        current.name.clone()
    } else if assets.maps.contains_key(START_MAP) {
        START_MAP.to_string()
    } else {
        assets.room_names.first().cloned().unwrap_or_default()
    };
    load.write(LoadMap {
        map,
        entry: Entry::Start,
    });
}

/// Where the player lands when a room loads, given how they entered it. Everything but
/// a fresh `Start` lands on a recorded destination cell (`cell`, already resolved to a
/// world centre and clamped in range): a door's `dest`, a teleporter pad, or a bench.
fn entry_position(entry: &Entry, start: Vec2, cell: Vec2) -> Vec2 {
    match entry {
        Entry::Start => start,
        Entry::Edge(..) | Entry::Teleport(..) | Entry::Bench(..) => cell,
    }
}

/// Spawn one enemy of `kind` at `pos` (the kind's sheet + tint; `anim` attaches the
/// atlas, `combat` drives the AI). Shared by the `E` glyph and arena combatants.
fn spawn_enemy(commands: &mut Commands, assets: &GameAssets, kind: usize, pos: Vec2) {
    let kind = kind.min(ENEMY_KINDS.len() - 1);
    commands.spawn((
        MapEntity,
        Enemy::new(kind),
        Hazard { half: ENEMY_HALF },
        Sprite {
            image: assets.enemy_sheets[kind].clone(),
            color: ENEMY_KINDS[kind].color,
            custom_size: Some(Vec2::splat(TILE)),
            ..default()
        },
        Transform::from_xyz(pos.x, pos.y, 2.0),
    ));
}

/// Spawn the entity for one grid glyph at `center`, returning `(entity, is_solid)` when
/// it's a standalone, **movable** cell (a solid, spike, bench, or rock spawner) so a
/// [`Mover`] can pick it up. Returns `None` for the start marker (which only sets
/// `start_pos`), enemies, fog, and empty cells — things a mover doesn't carry.
#[allow(clippy::too_many_arguments)] // distinct, glyph-specific spawn data
fn spawn_cell(
    commands: &mut Commands,
    assets: &GameAssets,
    map: &MapData,
    ch: char,
    col: i32,
    r: i32,
    center: Vec2,
    arena_armed: bool,
    start_pos: &mut Vec2,
) -> Option<(Entity, bool)> {
    if ch == START_MARKER {
        *start_pos = center;
        None
    } else if ch == ENEMY_GLYPH {
        // The type comes from the room's `enemies` array (by cell); an `E` with no entry
        // — or an out-of-range kind — uses the first type.
        let kind = map
            .enemies
            .iter()
            .find(|e| e.col == col && e.row == r)
            .map_or(0, |e| e.kind);
        spawn_enemy(commands, assets, kind, center);
        None
    } else if ch == BENCH_GLYPH {
        let e = commands
            .spawn((
                MapEntity,
                Bench { col, row: r },
                Sprite {
                    image: assets.bench.clone(),
                    custom_size: Some(Vec2::splat(TILE)),
                    ..default()
                },
                Transform::from_xyz(center.x, center.y, 1.0),
            ))
            .id();
        Some((e, false))
    } else if ch == FOG_GLYPH {
        // The mist is purely visual (you walk through it); the seal is the locked exits
        // below. Only show it while the arena is live.
        if arena_armed {
            crate::boss::spawn_fog_cell(commands, center);
        }
        None
    } else if map.solid.contains(ch) {
        let e = commands
            .spawn((
                MapEntity,
                Sprite {
                    image: assets.tile.clone(),
                    custom_size: Some(Vec2::splat(TILE)),
                    ..default()
                },
                Transform::from_xyz(center.x, center.y, 0.0),
            ))
            .id();
        Some((e, true))
    } else if map.spikes.contains(ch) {
        let e = commands
            .spawn((
                MapEntity,
                Hazard { half: SPIKE_HALF },
                Sprite {
                    image: assets.spikes.clone(),
                    custom_size: Some(Vec2::splat(TILE)),
                    ..default()
                },
                Transform::from_xyz(center.x, center.y, 1.0),
            ))
            .id();
        Some((e, false))
    } else if map.rocks.contains(ch) {
        // Stagger spawners so they don't all drop in sync.
        let mut timer = Timer::from_seconds(2.2, TimerMode::Repeating);
        timer.set_elapsed(std::time::Duration::from_secs_f32(
            (col as f32 * 0.41) % 2.2,
        ));
        let e = commands
            .spawn((
                MapEntity,
                RockSpawner { timer },
                Transform::from_xyz(center.x, center.y, 1.0),
            ))
            .id();
        Some((e, false))
    } else {
        None
    }
}

#[allow(clippy::too_many_arguments)] // a Bevy system; each param is a distinct query/resource
fn handle_load_map(
    mut commands: Commands,
    mut events: MessageReader<LoadMap>,
    map_assets: Res<Assets<MapData>>,
    assets: Res<GameAssets>,
    mut solids: ResMut<Solids>,
    mut cooldown: ResMut<TransitionCooldown>,
    mut current: ResMut<CurrentRoom>,
    mut room_view: ResMut<RoomView>,
    mut respawn: ResMut<RespawnPoint>,
    mut armed: ResMut<TeleportArmed>,
    lost: Res<LostEnergy>,
    cleared_bosses: Res<crate::boss::ClearedBosses>,
    cleared_arenas: Res<crate::boss::ClearedArenas>,
    mut boss_fight: ResMut<crate::boss::BossFight>,
    existing: Query<Entity, With<MapEntity>>,
    mut player: Query<(&mut Transform, &mut Velocity), With<Player>>,
) {
    let Some(load) = events.read().last().cloned() else {
        return;
    };

    // Clear the old room.
    for entity in &existing {
        commands.entity(entity).despawn();
    }
    solids.0.clear();

    let Some(handle) = assets.maps.get(&load.map) else {
        warn!("unknown map '{}'", load.map);
        return;
    };
    let Some(map) = map_assets.get(handle) else {
        warn!("map '{}' not loaded yet", load.map);
        return;
    };

    let height = map.tiles.len() as i32;
    let width = map
        .tiles
        .iter()
        .map(|r| r.chars().count())
        .max()
        .unwrap_or(0) as i32;
    let mut start_pos = Vec2::new(2.0 * TILE, 2.0 * TILE);
    // An arena arms on entry unless it's already cleared — permanently (a beaten boss)
    // or just for this checkpoint (any arena, until the next bench rest).
    let is_cleared = cleared_bosses.0.contains(&load.map) || cleared_arenas.0.contains(&load.map);
    let arena_armed = !map.fog_wall.is_empty() && !is_cleared;

    // Movers pick up whatever entity is authored at their cells (a solid, spike, bench,
    // …): map each cell to its mover, with a slot to collect the spawned parts.
    let to_world = |col: i32, row: i32| {
        Vec2::new(
            col as f32 * TILE + TILE / 2.0,
            (height - 1 - row) as f32 * TILE + TILE / 2.0,
        )
    };
    let mover_homes: Vec<Vec2> = map
        .movers
        .iter()
        .map(|m| to_world(m.tiles[0].0, m.tiles[0].1))
        .collect();
    let mut mover_at: HashMap<(i32, i32), usize> = HashMap::new();
    for (mi, m) in map.movers.iter().enumerate() {
        for &cell in &m.tiles {
            mover_at.insert(cell, mi);
        }
    }
    let mut mover_parts: Vec<Vec<(Entity, Vec2, bool)>> = vec![Vec::new(); map.movers.len()];

    for (r, line) in map.tiles.iter().enumerate() {
        let row = height - 1 - r as i32; // flip so row 0 is the top, y points up
        for (c, ch) in line.chars().enumerate() {
            let col = c as i32;
            let center = Vec2::new(
                col as f32 * TILE + TILE / 2.0,
                row as f32 * TILE + TILE / 2.0,
            );
            let Some((entity, solid)) = spawn_cell(
                &mut commands,
                &assets,
                map,
                ch,
                col,
                r as i32,
                center,
                arena_armed,
                &mut start_pos,
            ) else {
                continue;
            };
            if let Some(&mi) = mover_at.get(&(col, r as i32)) {
                // A mover cell: hand the entity to its platform, off the static grid.
                mover_parts[mi].push((entity, center - mover_homes[mi], solid));
            } else if solid {
                solids.0.insert((col, row));
            }
        }
    }

    // Hand each mover the parts it picked up.
    for (mi, m) in map.movers.iter().enumerate() {
        crate::movers::spawn_mover(
            &mut commands,
            m,
            height,
            mover_homes[mi],
            std::mem::take(&mut mover_parts[mi]),
        );
    }

    // Parallax scenery layers for this room (despawned with the room as MapEntity).
    crate::scenery::spawn_scenery(&mut commands, &assets, &map.scenery);

    // Teleporter pads are pure data (no grid glyph) — spawn one per portal at its
    // origin cell, flipping the row so y points up.
    for tp in &map.teleports {
        if (0..width).contains(&tp.origin_col) && (0..height).contains(&tp.origin_row) {
            let world_row = height - 1 - tp.origin_row;
            commands.spawn((
                MapEntity,
                Teleporter {
                    to: tp.to.clone(),
                    dest: (tp.dest_col, tp.dest_row),
                },
                Sprite {
                    image: assets.portal.clone(),
                    custom_size: Some(Vec2::splat(TILE)),
                    ..default()
                },
                Transform::from_xyz(
                    tp.origin_col as f32 * TILE + TILE / 2.0,
                    world_row as f32 * TILE + TILE / 2.0,
                    1.0,
                ),
            ));
        }
    }

    // If the player dropped a bloodstain in this room, place its marker so they can
    // walk back and reclaim the energy (see `crate::stats`).
    if lost.amount > 0 && lost.room == load.map {
        commands.spawn((
            MapEntity,
            Bloodstain,
            Sprite {
                image: assets.orb.clone(),
                custom_size: Some(Vec2::splat(20.0)),
                color: Color::srgb(0.7, 0.85, 1.0),
                ..default()
            },
            Transform::from_xyz(lost.pos.x, lost.pos.y, 3.0),
        ));
    }

    // Arm the arena: spawn its combatants (hidden until you got here) and seal the
    // exits. They were cleared above with the room, so a death/leave resets the fight.
    boss_fight.locked = arena_armed;
    boss_fight.respawn = map.fog_respawn;
    if arena_armed {
        for spawn in &map.fog_wall {
            let world_row = height - 1 - spawn.row;
            let pos = Vec2::new(
                spawn.col as f32 * TILE + TILE / 2.0,
                world_row as f32 * TILE + TILE / 2.0,
            );
            if spawn.boss {
                crate::boss::spawn_boss_at(&mut commands, &assets, spawn.kind, pos);
            } else {
                spawn_enemy(&mut commands, &assets, spawn.kind, pos);
            }
        }
    }

    // Resolve a destination cell (a door's `dest`, a teleporter pad, or a bench; grid
    // coords, row 0 = top) to a world centre, flipping the row so y points up.
    // Out-of-range falls back to `start`.
    let cell_pos = match load.entry {
        Entry::Edge(col, row) | Entry::Teleport(col, row) | Entry::Bench(col, row)
            if (0..width).contains(&col) && (0..height).contains(&row) =>
        {
            let world_row = height - 1 - row;
            Vec2::new(
                col as f32 * TILE + TILE / 2.0,
                world_row as f32 * TILE + TILE / 2.0,
            )
        }
        _ => start_pos,
    };

    let room = Vec2::new(width as f32 * TILE, height as f32 * TILE);
    let pos = entry_position(&load.entry, start_pos, cell_pos);

    // Record the room's name, doors + size for edge detection and the camera.
    *current = CurrentRoom {
        name: load.map.clone(),
        north: map.north.clone(),
        south: map.south.clone(),
        east: map.east.clone(),
        west: map.west.clone(),
        size: room,
    };
    room_view.size = room;
    room_view.snap = true;
    respawn.0 = pos; // dying returns the player to where they entered
    commands.insert_resource(ClearColor(map.bg_color()));

    if let Ok((mut transform, mut velocity)) = player.single_mut() {
        transform.translation.x = pos.x;
        transform.translation.y = pos.y;
        velocity.0 = Vec2::ZERO;
    }
    cooldown.0 = 0.4;
    // Disarm teleporters until the player steps clear, so they don't immediately
    // re-fire on the pad they just landed on (or one under the entry point).
    armed.0 = false;
}

fn tick_cooldown(time: Res<Time>, mut cooldown: ResMut<TransitionCooldown>) {
    cooldown.0 = (cooldown.0 - time.delta_secs()).max(0.0);
}

/// Swap rooms when the player walks off an edge that has a neighbour; if they fall
/// off a bottomless edge (no south neighbour), respawn them where they came in.
fn detect_transitions(
    cooldown: Res<TransitionCooldown>,
    current: Res<CurrentRoom>,
    boss_fight: Res<crate::boss::BossFight>,
    player: Query<&Transform, With<Player>>,
    mut load: MessageWriter<LoadMap>,
    mut hurt: MessageWriter<Hurt>,
) {
    // The fog gate seals the arena: no leaving (or falling out) mid-fight.
    if boss_fight.locked {
        return;
    }
    let Ok(transform) = player.single() else {
        return;
    };
    let pos = transform.translation.truncate();
    let room = current.size;

    if cooldown.0 <= 0.0 {
        // Pick which edge was crossed, then the door on it nearest the crossing point
        // (vertical edges sort by the player's `y`, horizontal edges by `x`).
        let door = if pos.x < 0.0 {
            nearest_door(&current.west, pos.y, room, true)
        } else if pos.x > room.x {
            nearest_door(&current.east, pos.y, room, true)
        } else if pos.y > room.y {
            nearest_door(&current.north, pos.x, room, false)
        } else if pos.y < 0.0 {
            nearest_door(&current.south, pos.x, room, false)
        } else {
            None
        };

        if let Some(door) = door {
            load.write(LoadMap {
                map: door.to.clone(),
                entry: Entry::Edge(door.dest.0, door.dest.1),
            });
            return;
        }
    }

    // Fell into a bottomless pit (no door below): take a hit. The damage system
    // respawns at the room entrance (no ground to knock back to), or the last bench
    // if it was the final heart.
    if pos.y < -TILE && current.south.is_empty() {
        hurt.write(Hurt::Pit);
    }
}

/// The door on an edge nearest the crossing point. `crossing` is the player's position
/// along the edge (world units): `y` on a `vertical` (east/west) edge, otherwise `x`.
fn nearest_door(doors: &[Door], crossing: f32, room: Vec2, vertical: bool) -> Option<&Door> {
    doors.iter().min_by(|a, b| {
        let axis = |d: &Door| {
            if vertical {
                // Grid row 0 is the top, so flip to a world `y`.
                room.y - (d.origin.1 as f32 + 0.5) * TILE
            } else {
                (d.origin.0 as f32 + 0.5) * TILE
            }
        };
        (axis(a) - crossing)
            .abs()
            .total_cmp(&(axis(b) - crossing).abs())
    })
}

/// While the boss arena is sealed, keep the player inside the room: the locked exits
/// leave door openings they could otherwise walk or fall out through.
fn confine_to_arena(
    boss_fight: Res<crate::boss::BossFight>,
    current: Res<CurrentRoom>,
    mut player: Query<&mut Transform, With<Player>>,
) {
    if !boss_fight.locked {
        return;
    }
    let Ok(mut transform) = player.single_mut() else {
        return;
    };
    let room = current.size;
    transform.translation.x = transform
        .translation
        .x
        .clamp(PLAYER_HALF.x, (room.x - PLAYER_HALF.x).max(PLAYER_HALF.x));
    // Floor level (one tile of border + the player's half height): catches a fall
    // through the arena's door gap and sets them back on the floor.
    transform.translation.y = transform.translation.y.max(TILE + PLAYER_HALF.y);
}

/// Send the player to a linked room when they step onto a teleporter pad. A pad
/// fires only when [`TeleportArmed`], which re-arms only once the player is
/// [`TELEPORT_REARM`] clear of every pad — the dead zone between that and the
/// (smaller) trigger stops a single pad chaining teleports or firing on arrival.
fn detect_teleport(
    mut armed: ResMut<TeleportArmed>,
    teleporters: Query<(&Transform, &Teleporter)>,
    player: Query<&Transform, With<Player>>,
    mut load: MessageWriter<LoadMap>,
) {
    let Ok(player_tf) = player.single() else {
        return;
    };
    let player_pos = player_tf.translation.truncate();

    // Re-arm only when well clear of every pad (not merely off the trigger box).
    let clear = teleporters
        .iter()
        .all(|(tf, _)| player_pos.distance(tf.translation.truncate()) > TELEPORT_REARM);
    if clear {
        armed.0 = true;
    }
    if !armed.0 {
        return;
    }

    // Armed and overlapping a pad → teleport, then disarm until clear again.
    if let Some((_, tp)) = teleporters.iter().find(|(tf, _)| {
        let delta = (tf.translation.truncate() - player_pos).abs();
        delta.x < TELEPORT_HALF.x + PLAYER_HALF.x && delta.y < TELEPORT_HALF.y + PLAYER_HALF.y
    }) {
        armed.0 = false;
        load.write(LoadMap {
            map: tp.to.clone(),
            entry: Entry::Teleport(tp.dest.0, tp.dest.1),
        });
    }
}

/// Show a prompt above a bench the player is standing on, and — when they press
/// **interact** there — open the **bench shop** (Rest / upgrade / Leave). The shop
/// itself (resting, buying) lives in [`crate::stats`]; here we just detect the bench
/// and hand it the cell to rest at.
fn bench_interact(
    intent: Res<PlayerIntent>,
    benches: Query<(&Transform, &Bench), Without<BenchPrompt>>,
    player: Query<&Transform, (With<Player>, Without<BenchPrompt>)>,
    mut prompt: Query<(&mut Transform, &mut Visibility), With<BenchPrompt>>,
    mut bench_at: ResMut<BenchAt>,
    mut mode: ResMut<OverlayMode>,
    mut menu: ResMut<NextState<CharMenu>>,
) {
    let Ok(player_tf) = player.single() else {
        return;
    };
    let player_pos = player_tf.translation.truncate();

    // The bench the player is standing on, if any.
    let on_bench = benches.iter().find(|(tf, _)| {
        let delta = (tf.translation.truncate() - player_pos).abs();
        delta.x < BENCH_HALF.x + PLAYER_HALF.x && delta.y < BENCH_HALF.y + PLAYER_HALF.y
    });

    // Show the prompt above that bench, or hide it.
    if let Ok((mut prompt_tf, mut visibility)) = prompt.single_mut() {
        match on_bench {
            Some((tf, _)) => {
                prompt_tf.translation.x = tf.translation.x;
                prompt_tf.translation.y = tf.translation.y + TILE;
                *visibility = Visibility::Visible;
            }
            None => *visibility = Visibility::Hidden,
        }
    }

    // On the interact press, open the bench shop for this bench.
    if intent.interact
        && let Some((_, bench)) = on_bench
    {
        bench_at.0 = Some((bench.col, bench.row));
        *mode = OverlayMode::Bench;
        menu.set(CharMenu::Open);
    }
}

/// Spawn the (initially hidden) bench prompt; [`bench_interact`] positions it.
fn spawn_bench_prompt(mut commands: Commands, existing: Query<(), With<BenchPrompt>>) {
    if !existing.is_empty() {
        return;
    }
    commands.spawn((
        BenchPrompt,
        Text2d::new("[E] bench"),
        TextFont {
            font_size: FontSize::Px(16.0),
            ..default()
        },
        TextColor(Color::srgb(0.96, 0.9, 0.6)),
        Transform::from_xyz(0.0, 0.0, 20.0),
        Visibility::Hidden,
    ));
}

fn despawn_bench_prompt(mut commands: Commands, prompts: Query<Entity, With<BenchPrompt>>) {
    for entity in &prompts {
        commands.entity(entity).despawn();
    }
}

